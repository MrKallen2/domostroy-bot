def get_material_props(material):
    """Возвращает (ширина, удельный вес) для выбранного материала"""
    materials = {
        "Брус": (200, 500),
        "Дерево": (200, 500),
        "Сип-панели": (1250, 600),
        "Каркас": (900, 500),
        "Кирпич": (120, 1600),
        "Газобетон": (300, 600)
    }
    return materials.get(material, (200, 500))